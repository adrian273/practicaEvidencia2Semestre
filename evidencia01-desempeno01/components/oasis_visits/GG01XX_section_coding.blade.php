<ul class="list-group clear-list font-sz-df">
    <li class="list-group-item fist-item">
        <b>Coding</b>
        <br>
        Safety and Quality of Performance – If helper assistance is required because
        patient’s performance is unsafe or of poor quality, score according to amount of
        assistance provided. <br>
        Activities may be completed with or without assistive devices.
    </li>
    <li class="list-group-item  fist-item">
        <b>06. Independent</b> – Patient completes the activity by him/herself
            with no assistance from a helper.
    </li>
    <li class="list-group-item">
        <b>05. Setup or clean-up assistance</b> – Helper sets up or cleans up; patient
        completes activity. Helper assists only prior to or following the activity.
        </li>
    <li class="list-group-item">
    <b>04. Supervision or touching assistance</b> –  Helper provides verbal cues and/or
        touching/steadying and/or contact guard assistance as patient completes activity.
        Assistance may be provided throughout the activity or intermittently.
    </li>
    <li class="list-group-item">
        <b>03. Partial/moderate assistance</b> – Helper does LESS THAN HALF the effort. Helper lifts, holds or supports trunk
        or limbs, but provides less than half the effort.
    </li>
    <li class="list-group-item">
        <b>02. Substantial/maximal assistance</b> - Helper does MORE THAN HALF the effort.
            Helper lifts or holds trunk or limbs and provides more than half the effort.
    </li>
    <li class="list-group-item">
        <b>01. Dependent</b> – Helper does ALL of the effort. Patient does none of the effort
            to complete the activity. Or, the assistance of 2 or more helpers is required for
            the patient to complete the activity.
    </li>
    <li class="list-group-item fist-item"><br>
        <b>If activity was not attempted, code reason:</b>
    </li>
    <li class="list-group-item fist-item">
        <b>07. Patient refused</b>
    </li>

    <li class="list-group-item">
        <b>09. Not applicable</b> - Not attempted and the patient did not perform this activity
            prior to the current illness, exacerbation or injury.
    </li>
    <li class="list-group-item">
        <b>10. Not attempted due to environmental limitations</b>
    </li>
    <li class="list-group-item">
        <b>88. Not attempted due to medical conditions or safety concerns</b>
    </li>
</ul>
